use hyper::mime::Mime;
use std::collections::{HashMap, HashSet};
use unicase::Ascii;

use mime::MimeRecord;

macro_rules! mimes {
    ( $( ($ext:expr, $mime:expr, $compressible:expr) ),* $(,)? ) => {
        {
            let mut map = ::std::collections::HashMap::new();
            $(
                {
                    let k = Ascii::new($ext);
                    let v = MimeRecord {
                        mime: $mime.parse().unwrap(),
                        compressible: $compressible
                    };

                    map.insert(k, v);
                }
            )*

            map
        }
    };
}

